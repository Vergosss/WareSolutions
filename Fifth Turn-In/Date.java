public class Date {
    
    // Class attributes
    private int day;
    private int month;
    private int year;


    // Constructor

 public Date(int day, int month, int year) {
    this.day = day;
    this.month = month;
    this.year = year;
    
}

 // The beginning of getters / setters:
 public int getday() {
    return this.day;
}

public void setday(int day) {
    this.day = day;
}


public int getmonth() {
    return this.month;
}

public void setmonth(int month) {
    this.month = month;
}


public int getyear() {
    return this.year;
}

public void setyear(int year) {
    this.year = year;
}


// end of getters and setters.
//

}
